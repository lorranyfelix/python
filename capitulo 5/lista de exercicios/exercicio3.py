def somar_numeros (valor1, valor2, valor3, valor4):
    variavel = 10
    soma = (valor1 + valor2 + valor3 + valor4) - variavel
    print(soma)

valor1 = int(input("Escreva o primeiro número: "))
valor2 = int(input("Escreva o segundo número: "))
valor3 = int(input("Escreva o terceiro número: "))
valor4 = int(input("Escreva o quarto número: "))

somar_numeros (valor1, valor2, valor3, valor4)