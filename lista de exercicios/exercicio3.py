raioCirculo = int(input("Digite o raio do círculo"))
pi = 3.14159
diametroCirculo = 2 * raioCirculo
areaCirculo = (pi) * raioCirculo ** 2
circuferenciaCirculo = 2 * pi

print("O diâmetro do círculo é", diametroCirculo)
print("A circuferência do círculo é", circuferenciaCirculo)
print("A área do círculo é", areaCirculo)